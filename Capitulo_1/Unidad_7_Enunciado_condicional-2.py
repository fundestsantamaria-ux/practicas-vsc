# Los enunciados condicionales permiten ejecutar diferentes bloques de código
#   basados en si una condición es verdadera o falsa. Además del "if" y "else",
#       Python también soporta "elif" (else if) para manejar múltiples condiciones.

# La estructura básica de un enunciado condicional con elif es:
# if condición1:
#     bloque de código si la condición1 es verdadera
# elif condición2:
#     bloque de código si la condición2 es verdadera
# else:
#     bloque de código si ninguna condición anterior es verdadera

datos = float(input("Ingrese un número: "))

if datos > 18 :

    print("Eres mayor de edad")
    
elif datos >= 65:
    print("Tienes descuento de jubilado ")
else:
    print("Eres menor de edad")



# La condición es una expresión que se evalúa como True o False.
# El bloque de código dentro del if, elif o else debe estar indentado (generalmente con 4 espacios).




# If : Tenemos un auto en una sola dirección

# Else: Tenemos un auto en dos direcciones

# Elif: Tenemos un auto en tres direcciones. Izquirda, derecha y recto


