# Variables y entradas
#---------------------------------

# Una variable es un espacio en la memoria que se utiliza para almacenar datos.


# En Python, no es necesario declarar el tipo de una variable antes de usarla, ya que Python 
#   es un lenguaje de tipado dinámico.

# Puedes asignar un valor a una variable utilizando el operador de asignación (=).

# El nombre de una variable debe comenzar con una letra o un guion bajo (_) y puede contener 
#   letras, números y guiones bajos.

# Las variables son sensibles a mayúsculas y minúsculas, lo que 
#   significa que "edad" y "Edad" son variables diferentes.


# Puedes cambiar el valor de una variable en cualquier momento durante la ejecución del programa.

# La función input() se utiliza para obtener entradas del usuario final


#  Esta función siempre devuelve una cadena (str), por lo que si necesitas un número, 
#       debes convertir la entrada utilizando int() para enteros o float() para decimales.

# Ejemplo de uso de variables y entradas


# Se pide la edad al usuario
edad = int(input("¿Cuántos años tienes? ")) # 30

# Se calcula la edad dentro del rango asignado
print("Dentro de 50 años tendrás", edad + 50)

# Enteros (int) 44, 234343
# Decimales (float) 3.14

