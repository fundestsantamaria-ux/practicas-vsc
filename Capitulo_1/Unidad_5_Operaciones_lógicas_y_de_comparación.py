# Los operadores lógicos y de comparación son fundamentales en la programación, ya que 
#   permiten tomar decisiones basadas en condiciones.

# Los operadores de comparación se utilizan para comparar dos valores
#    y devuelven un valor booleano (True o False).

# Los operadores lógicos se utilizan para combinar múltiples condiciones.

# Operadores de comparación comunes:
# - Igual a (==)
# - Diferente de (!=)   
# - Mayor que (>)
# - Menor que (<)
# - Mayor o igual que (>=)
# - Menor o igual que (<=)

# Operadores lógicos comunes:
# - Y (and): Devuelve True si ambas condiciones son verdaderas.
# - O (or): Devuelve True si al menos una de las condiciones es verdadera.
# - No (not): Invierte el valor booleano de una condición.

