# Que es la Programación Secuencial?
#---------------------------------
# La programación secuencial es un paradigma de programación donde las instrucciones se ejecutan en un
#   orden lineal, una tras otra. Es el enfoque más simple y directo para escribir código, ya que cada
#       instrucción se ejecuta en el orden en que aparece en el programa.

# En la programación secuencial, el flujo de control es lineal y no hay 
#   desvios, bucles o condiciones que alteren el orden de ejecución.





